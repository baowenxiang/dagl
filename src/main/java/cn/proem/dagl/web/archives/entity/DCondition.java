package cn.proem.dagl.web.archives.entity;

public class DCondition {
    
    private String fieldname;
    private String compair;
    private String val;
    
    public static final String EQ = "=";
    public static final String GT = ">";
    public static final String LT = "<";
    public static final String GTE = ">=";
    public static final String LTE = "<=";
    public static final String IN = "in";
    public static final String LIKE = "in";

    /**
     * 字段名
     * @return
     */
    public String getFieldname() {
        return fieldname;
    }
    
    /**
     * 字段名
     * @param fieldname 字段名
     */
    public void setFieldname(String fieldname) {
        this.fieldname = fieldname;
    }
    
    /**
     * 比较符
     * @return
     */
    public String getCompair() {
        return compair;
    }
    
    /**
     * 比较符
     * @param compair
     */
    public void setCompair(String compair) {
        this.compair = compair;
    }
    
    /**
     * 字段值
     * @return
     */
    public String getVal() {
        return val;
    }
    
    /**
     * 字段值
     * @param val 字段值
     */
    public void setVal(String val) {
        this.val = val;
    }
    
    
}
