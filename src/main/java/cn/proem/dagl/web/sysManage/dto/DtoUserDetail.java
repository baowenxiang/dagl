package cn.proem.dagl.web.sysManage.dto;

import cn.proem.core.entity.User;
import cn.proem.dagl.web.sysManage.entity.UserDetail;

/**
 * 获取个人信息
 * @author lenovo
 *
 */
public class DtoUserDetail {
	private User user;//用户
	
	private UserDetail userDetail;//用户详情
	
	private String lastTime;//上次登录时间
	private Integer loginNum;//登录次数
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	public UserDetail getUserDetail() {
		return userDetail;
	}
	public void setUserDetail(UserDetail userDetail) {
		this.userDetail = userDetail;
	}
	public String getLastTime() {
		return lastTime;
	}
	public void setLastTime(String lastTime) {
		this.lastTime = lastTime;
	}
	public Integer getLoginNum() {
		return loginNum;
	}
	public void setLoginNum(Integer loginNum) {
		this.loginNum = loginNum;
	}
	
	
	
}
