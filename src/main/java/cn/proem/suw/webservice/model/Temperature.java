package cn.proem.suw.webservice.model;

import java.util.Date;

public class Temperature {
    /**
     *  温度ID
     */
    private String id;
    /**
     *  记录日期
     */
    private Date jlrq;
    /**
     * 天气
     */
    private String tq;
    /**
     * 时间
     */
    private String wsdsj;
    /**
     * 记录人
     */
    private String jlr;
    /**
     * 温度
     */
    private String wd;
    /**
     * 湿度
     */
    private String sd;
    /**
     *  采取措施
     */
    private String cqcs;
    /**
     * 备注
     */
    private String bz;
    
    public String getId() {
        return id;
    }
    public Date getJlrq() {
        return jlrq;
    }
    public String getTq() {
        return tq;
    }
    public String getWsdsj() {
        return wsdsj;
    }
    public String getJlr() {
        return jlr;
    }
    public String getWd() {
        return wd;
    }
    public String getSd() {
        return sd;
    }
    public String getCqcs() {
        return cqcs;
    }
    public String getBz() {
        return bz;
    }
    public void setId(String id) {
        this.id = id;
    }
    public void setJlrq(Date jlrq) {
        this.jlrq = jlrq;
    }
    public void setTq(String tq) {
        this.tq = tq;
    }
    public void setWsdsj(String wsdsj) {
        this.wsdsj = wsdsj;
    }
    public void setJlr(String jlr) {
        this.jlr = jlr;
    }
    public void setWd(String wd) {
        this.wd = wd;
    }
    public void setSd(String sd) {
        this.sd = sd;
    }
    public void setCqcs(String cqcs) {
        this.cqcs = cqcs;
    }
    public void setBz(String bz) {
        this.bz = bz;
    }
}

